#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	int		i;
	int		j;
	size_t	a;

	i = ft_strlen(s1);
	j = 0;
	a = 0;
	while ((s2[j] != '\0') && (a < n))
	{
		s1[i + a] = s2[j];
		j++;
		a++;
	}
	s1[i + a] = '\0';
	return (s1);
}
